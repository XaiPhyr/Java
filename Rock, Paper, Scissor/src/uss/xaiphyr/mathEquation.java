package uss.xaiphyr;

public class mathEquation {

    public static int add(int a, int b){
        int total = a + b;
        return total;
    }

    public static int sub(int a, int b){
        int total = a - b;
        return total;
    }

    public static int mul(int a, int b){
        int total = a * b;
        return total;
    }

    public static int div(int a, int b){
        int total = a / b;
        return total;
    }
}
